let num = 50

console.log("Tabuada de Adição")
for(let i = 0; i <= 10; i++)
    console.log(num, "+", i, "= ", num + i)
console.log("Tabuada de Subtração")
for(let i = 0; i <= 10; i++)
    console.log(num, "-", i, "= ",num - i)
console.log("Tabuada de Divisão")
for(let i = 1; i <= 10; i++)
    console.log(num, "/", i, "= ",num / i)
console.log("Tabuada de Multiplicação")
for(let i = 0; i <= 10; i++)
    console.log(num, "x", i, "= ",num * i)